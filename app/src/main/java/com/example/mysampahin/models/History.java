package com.example.mysampahin.models;

public class History {
    private String imgSampah;
    private String tglTransaksi;
    private String jenisSampah;
    private String waktuSampah;

    public String getWaktuSampah() {
        return waktuSampah;
    }

    public void setWaktuSampah(String waktuSampah) {
        this.waktuSampah = waktuSampah;
    }

    public String getImgSampah() {
        return imgSampah;
    }

    public void setImgSampah(String imgSampah) {
        this.imgSampah = imgSampah;
    }

    public String getTglTransaksi() {
        return tglTransaksi;
    }

    public void setTglTransaksi(String tglTransaksi) {
        this.tglTransaksi = tglTransaksi;
    }

    public String getJenisSampah() {
        return jenisSampah;
    }

    public void setJenisSampah(String jenisSampah) {
        this.jenisSampah = jenisSampah;
    }



}
